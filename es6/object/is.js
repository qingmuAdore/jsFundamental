/********************************************************
Object.is()

* 类似 ===  (比较类型,若对象则比较指向的地址)
* NaN 相等, +0不等于-0
********************************************************/

Object.is('foo', 'foo') // true
Object.is({}, {}) // false ,指向的地址不一样

+0 === -0 //true
NaN === NaN // false

Object.is(+0, -0) // false
Object.is(NaN, NaN) // true
